public class PurpleBlock {

    String purpTopic = "";
    String purp1 = "";
    String purp2 = "";
    String purp3 = "";
    String purp4 = "";

    public PurpleBlock(String pinT,String pin1, String pin2, String pin3, String pin4){
        
        purpTopic = pinT;
        purp1 = pin1;
        purp2 = pin2;
        purp3 = pin3;
        purp4 = pin4;
    }

    public String retYe1(){
        return purp1;
    }
    public String retYe2(){
        return purp2;
    }
    public String retYe3(){
        return purp3;
    }
    public String retYe4(){
        return purp4;
    }
    
}
